import re

last, name, sur = list(map(str.strip, input().split()))
res = re.sub(fr'{last}\w*\s{name[:-2]}\w*\s{sur}\w*|{last}\w*\s{name[0]}\.\s{sur[0]}\.', 'ФИО', input())
print(res)
