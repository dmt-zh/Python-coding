# Выведите всё валидное время, которое находится в строке.

# Sample Input 1:
# 13:79 24:33 02:60 03:81 23:59 28:64 46:50 14:39 91:19 13:35 02:57 10:10 00:00 59:59 15:51

# Sample Output 1:
# 23:59
# 14:39
# 13:35
# 02:57
# 10:10
# 00:00
# 15:51



import re

valid_time = re.findall(r'[0-1][0-9]:[0-5][0-9]|2[0-3]:[0-5][0-9]', input())
print(*valid_time, sep='\n')